# 写在前面：
# 命名规范对于提高代码的可读性非常重要。好的变量、函数或类名几乎可以代替注释。对于模块、类、
# 函数、变量、常量的命名，建议遵循如下原则：

# 1.名字要尽可能精准表达所代表的对象的含义。
# 2.名字不要和已有的模块、类、函数或变量名重复。
# 3.模块名使用小写字母命名，或首字母小写，不要用下划线。
# 4.类名使用驼峰（CamelCase）命名风格，首字母大写。
# 5.类的成员名小写，私有成员以两个下划线开头，保护成员以一个下划线开头。
# 6.函数名小写，如有多个单词，以下划线分隔。
# 7.变量名小写，如有多个单词，以下划线分隔。
# 8.常量名大写，如有多个单词，以下划线分隔。