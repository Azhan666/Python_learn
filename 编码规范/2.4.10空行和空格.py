# 写在前面：
# 合理的使用空行和空格，会让代码产生节奏感，可以显著提高代码的可读性。不用担心额外的空行和空格
# 会给解释器带来负担，解释器会自动忽略忽略多余的空行和空格。对于空行和空格的使用，建议遵循如下
# 原则：

# 1.编码格式声明、模块导入、常量和全局变量声明、顶级定义和执行代码之间空两行。
# 2.顶级定义之间空两行，方法定义之间空一行。
# 3.在函数和方法内部必要的地方可以空一行以增加节奏感，但应避免连续空行。
# 4.在二元运算符两边各空一格，算术运算符两边的空格可灵活使用，但两侧务必要保持一致。
# 5.不要在逗号、分号、冒号前面加空格，但应该在他们后面加（除非在行尾）。
# 6.函数的参数列表中，逗号之后要有空格。
# 7.函数的参数列表中，默认值等号两边不要添加空格。
# 8.参数列表、索引或切片的左括号前不应加空格。